<script>
document.addEventListener('DOMContentLoaded', function () {
    const sections = document.querySelectorAll('.rule-section');
    const navLinks = document.querySelectorAll('#niyamawaliNav .nav-link');

    const observer = new IntersectionObserver(entries => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                navLinks.forEach(link => {
                    link.classList.remove('active');
                    if (link.getAttribute('href').substring(1) === entry.target.id) {
                        link.classList.add('active');
                    }
                });
            }
        });
    }, { rootMargin: "-50% 0px -50% 0px" });

    sections.forEach(section => {
        observer.observe(section);
    });

    // Smooth scroll for sidebar links
    navLinks.forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            const targetElement = document.querySelector(targetId);
            if(targetElement) {
                // Collapse the target accordion item if it's not already shown
                const collapseElement = new bootstrap.Collapse(targetElement.querySelector('.accordion-collapse'), {
                    toggle: false
                });
                collapseElement.show();

                // Scroll into view
                const headerOffset = 90; // Adjust for sticky header
                const elementPosition = targetElement.getBoundingClientRect().top;
                const offsetPosition = elementPosition + window.pageYOffset - headerOffset;

                window.scrollTo({
                    top: offsetPosition,
                    behavior: "smooth"
                });
            }
        });
    });
});
</script>
